def calculate_perimeter(length, width):
    """Calculates the perimeter of a rectangle.

    Args:
        length: The length of the rectangle.
        width: The width of the rectangle.

    Returns:
        The perimeter of the rectangle.
    """
    return 2 * (length + width)

